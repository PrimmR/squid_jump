byte cursorx = 0;
byte cursory = 0;

void printletter(char letter) {
  byte index;
  if (letter >= 0x41 && letter <= 0x5a) {
    index = letter - 0x41;
  } else if (letter >= 0x30 && letter <= 0x39) {
    index = letter - 0x16;
  } else if (letter == 0x21) {  // !
    index = 0x24;
  } else if (letter == 0x2b) {  // +
    index = 0x25;
  } else if (letter == 0x3a) {  // :
    index = 0x26;
  } else {
    cursorx += CHAR_WIDTH;
    return;
  }

  Sprites::drawOverwrite(cursorx, cursory, Font, index);
  cursorx += CHAR_WIDTH;
}

void squidprint(char* string) {
  for (int i = 0; i < strlen(string); i++) {
    char letter = toupper(string[i]);
    printletter(letter);
  }
}

void squidprint(int num) {
  char cstr[16];
  squidprint(itoa(num, cstr, 10));
}

void setcursor(byte x, byte y) {
  cursorx = x;
  cursory = y;
}